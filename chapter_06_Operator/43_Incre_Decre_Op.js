// Pre Increment
let a = 10;
let b = ++a; // (increment the a by 1)

// // Pre increment -> ++a ->. increse the value and then assign.
console.log(b);
console.log(a);


// // Deincrement
let c = 10;
let d = --c;  // (increment the a by 1)
console.log(c); //9
console.log(d); //9

let e = 10;
let f = e--;
console.log(f); //10
console.log(e);  //9
